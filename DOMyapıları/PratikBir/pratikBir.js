function js_style() {
  const colorChange = document.getElementById("jsstyle");
  colorChange.style.backgroundColor = "pink";

  const changeParagraph = document.getElementById("text");
  changeParagraph.style.color = "red";
  changeParagraph.style.fontSize = "20px"; // Yazı boyutu 20 piksel olarak ayarlandı
  changeParagraph.style.fontFamily = "Arial, sans-serif"; // Yazı tipi değiştirildi
}
